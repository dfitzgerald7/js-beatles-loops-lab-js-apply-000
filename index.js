function theBeatlesPlay(musArray, insArray ){
  var myArray = [];
  for (i=0; i < musArray; i++){
    var musician = musArray[i];
    var instrument = insArray[i];
    var myString = musician + " plays " + instrument;
    myArray[i] = myString;
  }
  return myArray;
}

